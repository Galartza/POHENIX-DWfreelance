// Función para abrir el menú móvil
function openNav() {
    document.getElementById("mobile-menu").style.width = "50%"; // Establecer el ancho del menú móvil al 50%
}

// Función para cerrar el menú móvil
function closeNav() {
    document.getElementById("mobile-menu").style.width = "0%"; // Establecer el ancho del menú móvil a 0%
}